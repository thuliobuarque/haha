package com.accenture.treinamento.portal.model;

public class Escolaridade {
	private Integer id_escolaridade;
	private String descEscolaridade;
	
	
	
	public Integer getId_escolaridade() {
		return id_escolaridade;
	}
	public void setId_escolaridade(Integer id_escolaridade) {
		this.id_escolaridade = id_escolaridade;
	}
	public String getDescEscolaridade() {
		return descEscolaridade;
	}
	public void setDescEscolaridade(String descEscolaridade) {
		this.descEscolaridade = descEscolaridade;
	}
	
	
}
